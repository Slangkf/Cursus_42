#include "libft.h"

int	ft_strisalpha(char *s)
{
	int		i;
	char	c;

	i = 0;
	while (s[i])
	{
		c = s[i];
		if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
			return (1);
		i++;
	}
	return (0);
}

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}
